// Function to translate text using the translation dictionary
function translateText(text) {
  if (!text || typeof text !== 'string') return text;
  
  let translatedText = text;
  
  // Apply translations
  for (const [english, swedish] of Object.entries(descriptionTranslations)) {
    const regex = new RegExp(`\\b${english}\\b`, 'gi');
    translatedText = translatedText.replace(regex, swedish);
  }
  
  return translatedText;
}

// Function to translate a single workflow file
function translateWorkflow(filePath) {
  try {
    console.log(`🔄 Translating: ${path.basename(filePath)}`);
    
    const content = fs.readFileSync(filePath, 'utf8');
    const workflow = JSON.parse(content);
    
    // Get the original name without .json extension
    const originalName = path.basename(filePath, '.json');
    
    // Translate the main name and description
    if (workflowTranslations[originalName]) {
      workflow.name = workflowTranslations[originalName];
      console.log(`   ✅ Name: "${originalName}" → "${workflow.name}"`);
    }
    
    if (workflow.description) {
      const originalDesc = workflow.description;
      workflow.description = translateText(originalDesc);
      if (originalDesc !== workflow.description) {
        console.log(`   ✅ Description translated`);
      }
    }
    
    // Translate README and note descriptions in nodes
    if (workflow.data && workflow.data.nodes) {
      workflow.data.nodes.forEach(node => {
        if (node.data && node.data.node && node.data.node.description) {
          const originalDesc = node.data.node.description;
          node.data.node.description = translateText(originalDesc);
          if (originalDesc !== node.data.node.description) {
            console.log(`   ✅ Node description translated`);
          }
        }
      });
    }
    
    // Create backup
    const backupPath = `${filePath}.backup-${Date.now()}`;
    fs.copyFileSync(filePath, backupPath);
    
    // Save translated workflow
    fs.writeFileSync(filePath, JSON.stringify(workflow, null, 2));
    console.log(`   💾 Saved: ${path.basename(filePath)}`);
    
    return true;
  } catch (error) {
    console.error(`   ❌ Error translating ${path.basename(filePath)}: ${error.message}`);
    return false;
  }
}

// Main function to translate all workflows
function translateAllWorkflows() {
  const workflowsDir = 'src/backend/base/axiestudio/initial_setup/starter_projects';
  
  if (!fs.existsSync(workflowsDir)) {
    console.error(`❌ Workflows directory not found: ${workflowsDir}`);
    return;
  }
  
  const files = fs.readdirSync(workflowsDir).filter(file => file.endsWith('.json'));
  
  console.log(`🇸🇪 Starting translation of ${files.length} workflow templates...`);
  
  let successCount = 0;
  let errorCount = 0;
  
  files.forEach(file => {
    const filePath = path.join(workflowsDir, file);
    if (translateWorkflow(filePath)) {
      successCount++;
    } else {
      errorCount++;
    }
  });
  
  console.log(`\n🎉 Translation Complete!`);
  console.log(`   ✅ Successfully translated: ${successCount} workflows`);
  console.log(`   ❌ Errors: ${errorCount} workflows`);
  console.log(`   📊 Total: ${files.length} workflows`);
  
  if (successCount > 0) {
    console.log(`\n🇸🇪 All workflow templates are now available in Swedish!`);
  }
}

// Run the translation
translateAllWorkflows();
